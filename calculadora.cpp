#include "calculadora.h"

class Calculadora {

public:
    int Sumar(int x, int y) {
        return x + y;
    }

    int Restar(int x, int y) {
        return x - y;
    }

    int Multiplicar(int x, int y) {
        return x * y;
    }

    int Dividir(int x, int y) {
        return x / y;
    }

};
